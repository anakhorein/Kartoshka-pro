import Foundation

/// Структура, представляющая категорию продуктов питания
///
/// Эта структура используется для классификации продуктов питания по категориям
/// и поддерживает многоязычность через отдельные поля для русского и английского названий.
///
/// ## Пример использования
/// ```swift
/// let category = Category(id: "1", title: "Фрукты", title_en: "Fruits")
/// print(category.title) // Выведет: "Фрукты"
/// ```
///
/// ## Важные замечания
/// - Структура поддерживает кодирование и декодирование (Codable)
/// - Реализует протоколы Identifiable и Hashable для использования в SwiftUI
/// - Все поля являются обязательными
/// - Поддерживает двуязычность (русский и английский)
struct Category: Codable, Identifiable, Hashable {
    /// Уникальный идентификатор категории
    ///
    /// Используется для однозначной идентификации категории в системе
    let id: String
    
    /// Название категории на русском языке
    ///
    /// Основное название категории, используемое в русскоязычном интерфейсе
    let title: String
    
    /// Название категории на английском языке
    ///
    /// Альтернативное название категории на английском языке
    let title_en: String
} 